
import 'package:carousel_slider/carousel_slider.dart';
import 'package:flutter/material.dart';
import 'package:online_music_player/theme/colors.dart';

class CustomeCarouselHomePage extends StatefulWidget {
  final List<String> items;

  CustomeCarouselHomePage({this.items});

  @override
  _CustomeCarouselHomePageState createState() =>
      _CustomeCarouselHomePageState();
}

class _CustomeCarouselHomePageState extends State<CustomeCarouselHomePage> {
  int activeIndex = 0;
  setActiveDot(index) {
    setState(() {
      activeIndex = index;
    });
  }

  @override
  Widget build(BuildContext context) {
    return Stack(
      overflow: Overflow.visible,
      children: <Widget>[
        Container(
          width: MediaQuery.of(context).size.width,
          child: CarouselSlider(
            onPageChanged: (index) {
              setActiveDot(index);
            },
            autoPlayCurve: Curves.fastLinearToSlowEaseIn,
            autoPlayAnimationDuration: Duration(seconds: 2),
            viewportFraction: 0.9,
            // enlargeCenterPage: true,
            items: widget.items.map((item) {
              return Builder(
                builder: (BuildContext context) {
                  return
                  Card(
                  shape: BeveledRectangleBorder(
                    borderRadius: BorderRadius.circular(5.0),
                  ),
                  child: Container(
                    width: MediaQuery.of(context).size.width,
                     child: Image(
                          image: NetworkImage(item),
                          fit: BoxFit.cover,
                        ),
                    decoration: BoxDecoration(borderRadius: BorderRadius.circular(5)),
                    //  child: Text('text $i', style: TextStyle(fontSize: 16.0),)
                  ),
                );
                  // return Stack(
                  //   children: <Widget>[
                  //     Container(
                  //       width: MediaQuery.of(context).size.width,
                  //       child: Image(
                  //         image: NetworkImage(item),
                  //         fit: BoxFit.cover,
                  //       ),
                  //     ),
                  //     Container(
                  //       width: MediaQuery.of(context).size.width,
                  //       color: Colors.black.withOpacity(0.2),
                  //     ),
                  //   ],
                  // );
                },
              );
            }).toList(),
          ),
        ),
        // Positioned(
        //   left: 0,
        //   right: 0,
        //   bottom: 10,
        //   child: Row(
        //       mainAxisAlignment: MainAxisAlignment.end,
        //       children: List.generate(widget.items.length, (idx) {
        //         return activeIndex == idx ? ActiveDot() : InactiveDot();
        //       })),
        // )
      ],
    );
  }
}

class ActiveDot extends StatelessWidget {
  @override
  Widget build(BuildContext context) {
    return Padding(
      padding: const EdgeInsets.only(right: 8.0),
      child: Container(
        width: 25,
        height: 8,
        decoration: BoxDecoration(
          color: white,
          borderRadius: BorderRadius.circular(5),
        ),
      ),
    );
  }
}

class InactiveDot extends StatelessWidget {
  @override
  Widget build(BuildContext context) {
    return Padding(
      padding: const EdgeInsets.only(right: 8.0),
      child: Container(
        width: 8,
        height: 8,
        decoration: BoxDecoration(
          color: grey,
          borderRadius: BorderRadius.circular(5),
        ),
      ),
    );
  }
}
